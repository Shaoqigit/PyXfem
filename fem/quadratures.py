from abc import abstractmethod, ABCMeta
import numpy as np

class NumericalQuadrature(metaclass=ABCMeta):
    """base abstract numerical quadrature class
    """
    @abstractmethod
    def points(self):
        pass

    @abstractmethod
    def weights(self):
        pass

